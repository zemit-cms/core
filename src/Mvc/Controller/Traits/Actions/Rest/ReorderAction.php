namespace Zemit\Mvc\Controller\Traits\Actions\Rest;

use Exception;
use Phalcon\Filter\Filter;
use Phalcon\Http\ResponseInterface;
use Phalcon\Mvc\ModelInterface;
use Zemit\Mvc\Controller\Traits\Abstracts\AbstractExpose;
use Zemit\Mvc\Controller\Traits\Abstracts\AbstractQuery;
use Zemit\Mvc\Controller\Traits\Abstracts\AbstractInjectable;
use Zemit\Mvc\Controller\Traits\Abstracts\AbstractParams;
use Zemit\Mvc\Controller\Traits\Abstracts\AbstractRestResponse;
use Zemit\Mvc\Model\Interfaces\PositionInterface;

trait ReorderAction
{
    use AbstractExpose;
    use AbstractQuery;
    use AbstractInjectable;
    use AbstractParams;
    use AbstractRestResponse;
    
    /**
     * Reorder the entity based on the provided position.
     *
     * @return ResponseInterface The response generated by the method.
     * @throws \Phalcon\Filter\Exception
     * @throws Exception
     */
    public function reorderAction(): ResponseInterface
    {
        $entity = $this->findFirst();
        
        if (!$entity) {
            return $this->setRestErrorResponse(404);
        }
        
        $position = $this->getParam('position', [Filter::FILTER_INT]);
        
        assert($entity instanceof PositionInterface);
        assert($entity instanceof ModelInterface);
        $reordered = $entity->reorder($position);
        
        $this->view->setVars([
            'reordered' => $reordered,
            'data' => $this->expose($entity),
            'messages' => $entity->getMessages(),
        ]);
        
        return $this->setRestResponse($reordered);
    }
}
